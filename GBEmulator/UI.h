enum class UIAction {
    None,
    LoadROM,
    TogglePause,
    Reset,
    Quit
};

struct UIButton {
    SDL_Rect rect;
    std::string label;
    UIAction action;

    UIButton(int x, int y, int w, int h, const std::string& lbl, UIAction act)
        : rect{ x, y, w, h }, label(lbl), action(act) {}

    bool contains(int mx, int my) const {
        return mx >= rect.x && mx < rect.x + rect.w &&
               my >= rect.y && my < rect.y + rect.h;
    }
};

inline void drawChar(SDL_Renderer* renderer, int x, int y, char ch, int scale = 1) {
    if (ch < 32 || ch > 126) return;
    const uint8_t* glyph = FONT_8X8[ch - 32];
    for (int row = 0; row < 8; row++) {
        for (int col = 0; col < 8; col++) {
            if (glyph[row] & (0x80 >> col)) {
                SDL_Rect pixel = { x + col * scale, y + row * scale, scale, scale };
                SDL_RenderFillRect(renderer, &pixel);
            }
        }
    }
}

inline void drawText(SDL_Renderer* renderer, int x, int y, const std::string& text, int scale = 1) {
    for (size_t i = 0; i < text.length(); i++) {
        drawChar(renderer, x + (int)i * 8 * scale, y, text[i], scale);
    }
}

inline void drawButton(SDL_Renderer* renderer, const UIButton& btn, bool hovered, bool active) {
    if (active) {
        SDL_SetRenderDrawColor(renderer, 180, 60, 60, 255);
    } else if (hovered) {
        SDL_SetRenderDrawColor(renderer, 100, 100, 100, 255);
    } else {
        SDL_SetRenderDrawColor(renderer, 70, 70, 70, 255);
    }
    SDL_RenderFillRect(renderer, &btn.rect);

    SDL_SetRenderDrawColor(renderer, 140, 140, 140, 255);
    SDL_RenderDrawRect(renderer, &btn.rect);

    int textW = (int)btn.label.length() * 8;
    int textX = btn.rect.x + (btn.rect.w - textW) / 2;
    int textY = btn.rect.y + (btn.rect.h - 8) / 2;
    SDL_SetRenderDrawColor(renderer, 255, 255, 255, 255);
    drawText(renderer, textX, textY, btn.label);
}
